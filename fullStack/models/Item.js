const mongoose = require( 'mongooose' );
const Schema = mongoose.Schema;

const ItemSchema = new Schema( {
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    category: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
} );

module.exports = Item = mongoose.model( 'item', ItemSchema );